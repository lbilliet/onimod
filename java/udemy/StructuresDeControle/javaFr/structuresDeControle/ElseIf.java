package structuresDeControle;

public class ElseIf {
	
	public static void main(String... args) {

		int nbPlanetes = 8;
		if (nbPlanetes == 8) {
			System.out.println("Aux dernières nouvelles, le nombre total de planètes dans le système solaire est de :"
					+ nbPlanetes);
		} else if (nbPlanetes == 9) {
			System.out.println("Il y a quelques années cependant, elles étaient au nombre de : " + nbPlanetes);

		} else {
			System.out.println("Au cours de l'ère moderne, le nombre de planètes n'a jamais été officiellement de : "
					+ nbPlanetes);
		}

		nbPlanetes++;
		if (nbPlanetes == 8) {
			System.out.println("Aux dernières nouvelles, le nombre total de planètes dans le système solaire est de :"
					+ nbPlanetes);
		} else if (nbPlanetes == 9) {
			System.out.println("Il y a quelques années cependant, elles étaient au nombre de : " + nbPlanetes);

		} else {
			System.out.println("Au cours de l'ère moderne, le nombre de planètes n'a jamais été officiellement de : "
					+ nbPlanetes);
		}
	}

}
